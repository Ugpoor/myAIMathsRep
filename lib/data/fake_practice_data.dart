  static List<PracticeRecord> getAllPracticeRecords() => practiceRecords;

  static PracticeRecord? getPracticeRecordById(String id) {
    return practiceRecords.firstWhere(
      (p) => p.id == id,
      orElse: () => PracticeRecord(
        id: '',
        title: '',
        topic: '',
        generatedAt: DateTime.now(),
        status: '',
        students: [],
      ),
    );
  }

  static List<PracticeStudentRecord> getStudentRecordsByPracticeId(
    String practiceId,
  ) {
    return practiceRecords
        .where((p) => p.id == practiceId)
        .expand((p) => p.students)
        .toList();
  }

  static List<PracticeQuestion> getQuestionsByPracticeAndStudent(
    String practiceId,
    String studentId,
  ) {
    return practiceQuestions
        .where((q) => q.practiceId == practiceId && q.studentId == studentId)
        .toList();
  }
